#!/usr/bin/env sh
set -eu

RUN_PREFIX="${RUN_PREFIX:-eval$(date +%Y%m%d%H%M%S)}"
REPORT_DIR="${REPORT_DIR:-load/reports}"
API_BASE="${API_BASE:-http://host.docker.internal:8080}"
WS_BASE="${WS_BASE:-ws://host.docker.internal:8081}"
API_HEALTH_TIMEOUT_SECONDS="${API_HEALTH_TIMEOUT_SECONDS:-60}"

ONLINE_USER_STEPS="${ONLINE_USER_STEPS:-1000 5000 10000 20000}"
ONLINE_DURATION="${ONLINE_DURATION:-90s}"
ONLINE_SOCKET_HOLD_MS="${ONLINE_SOCKET_HOLD_MS:-90000}"
ONLINE_PING_INTERVAL_MS="${ONLINE_PING_INTERVAL_MS:-10000}"

THROUGHPUT_USERS="${THROUGHPUT_USERS:-1000}"
THROUGHPUT_SEND_INTERVAL_MS="${THROUGHPUT_SEND_INTERVAL_MS:-1000}"
THROUGHPUT_DURATION="${THROUGHPUT_DURATION:-2m}"
THROUGHPUT_SOCKET_LIFE_MS="${THROUGHPUT_SOCKET_LIFE_MS:-120000}"
THROUGHPUT_MAX_PENDING_ACKS="${THROUGHPUT_MAX_PENDING_ACKS:-20}"

CONNECT_SUCCESS_MIN="${CONNECT_SUCCESS_MIN:-99}"
UNEXPECTED_CLOSE_MAX="${UNEXPECTED_CLOSE_MAX:-0}"
PONG_SUCCESS_MIN="${PONG_SUCCESS_MIN:-95}"
PONG_P95_MAX="${PONG_P95_MAX:-1}"
ACK_ERROR_MAX="${ACK_ERROR_MAX:-1}"
ACK_P95_MAX="${ACK_P95_MAX:-1}"
DB_RATIO_MIN="${DB_RATIO_MIN:-98}"

mkdir -p "$REPORT_DIR"

ONLINE_PREFIX="${RUN_PREFIX}-online"
THROUGHPUT_RUN_ID="${RUN_PREFIX}-1000u1mps"
SUMMARY_MD="$REPORT_DIR/evaluation-criteria-benchmark-${RUN_PREFIX}.md"

write_summary() {
  RUN_PREFIX="$RUN_PREFIX" \
  ONLINE_PREFIX="$ONLINE_PREFIX" \
  THROUGHPUT_RUN_ID="$THROUGHPUT_RUN_ID" \
  REPORT_DIR="$REPORT_DIR" \
  THROUGHPUT_USERS="$THROUGHPUT_USERS" \
  THROUGHPUT_SEND_INTERVAL_MS="$THROUGHPUT_SEND_INTERVAL_MS" \
  ACK_ERROR_MAX="$ACK_ERROR_MAX" \
  ACK_P95_MAX="$ACK_P95_MAX" \
  DB_RATIO_MIN="$DB_RATIO_MIN" \
  CONNECT_SUCCESS_MIN="$CONNECT_SUCCESS_MIN" \
  UNEXPECTED_CLOSE_MAX="$UNEXPECTED_CLOSE_MAX" \
  PONG_SUCCESS_MIN="$PONG_SUCCESS_MIN" \
  PONG_P95_MAX="$PONG_P95_MAX" \
  DB_COUNT="${DB_COUNT:-}" \
  node load/summarize-evaluation-benchmark.js
}

echo "Evaluation benchmark: ${RUN_PREFIX}"
echo "Online steps: ${ONLINE_USER_STEPS}"
echo "Throughput target: ${THROUGHPUT_USERS} users, ${THROUGHPUT_SEND_INTERVAL_MS}ms interval"

set +e
USER_STEPS="$ONLINE_USER_STEPS" \
DURATION="$ONLINE_DURATION" \
SOCKET_HOLD_MS="$ONLINE_SOCKET_HOLD_MS" \
PING_INTERVAL_MS="$ONLINE_PING_INTERVAL_MS" \
API_BASE="$API_BASE" \
WS_BASE="$WS_BASE" \
API_HEALTH_TIMEOUT_SECONDS="$API_HEALTH_TIMEOUT_SECONDS" \
RUN_PREFIX="$ONLINE_PREFIX" \
REPORT_DIR="$REPORT_DIR" \
CONNECT_SUCCESS_MIN="$CONNECT_SUCCESS_MIN" \
UNEXPECTED_CLOSE_MAX="$UNEXPECTED_CLOSE_MAX" \
PONG_SUCCESS_MIN="$PONG_SUCCESS_MIN" \
PONG_P95_MAX="$PONG_P95_MAX" \
sh load/find-online-ceiling.sh
ONLINE_EXIT_CODE="$?"
set -e

if [ "$ONLINE_EXIT_CODE" -ne 0 ]; then
  echo "Warning: online ceiling stage exited with code ${ONLINE_EXIT_CODE}; continuing to throughput benchmark."
fi

write_summary

docker run --rm \
  -v "$PWD/load:/scripts" \
  grafana/k6 run \
  -e API_BASE="$API_BASE" \
  -e WS_BASE="$WS_BASE" \
  -e API_HEALTH_TIMEOUT_SECONDS="$API_HEALTH_TIMEOUT_SECONDS" \
  -e USERS="$THROUGHPUT_USERS" \
  -e DURATION="$THROUGHPUT_DURATION" \
  -e SEND_INTERVAL_MS="$THROUGHPUT_SEND_INTERVAL_MS" \
  -e SOCKET_LIFE_MS="$THROUGHPUT_SOCKET_LIFE_MS" \
  -e MAX_PENDING_ACKS="$THROUGHPUT_MAX_PENDING_ACKS" \
  -e RUN_ID="$THROUGHPUT_RUN_ID" \
  -e REPORT_DIR=/scripts/reports \
  /scripts/ws-chat-load.js

THROUGHPUT_JSON="$REPORT_DIR/ws-chat-load-${THROUGHPUT_RUN_ID}.json"
DB_COUNT="$(docker compose exec -T postgres psql -U admin -d imdb -t -A -c "SELECT count(*) FROM \"Message\" WHERE \"requestId\" LIKE 'k6-${THROUGHPUT_RUN_ID}-%';" | tr -d '[:space:]')"
write_summary

echo ""
echo "Benchmark summary:"
echo "$SUMMARY_MD"
